macas = int(input("Digite a quantidade de maçãs vendidas: "))
bananas = int(input("Digite a quantidade de maçãs vendidas: "))

if macas > bananas:
    input("As maçãs tiveram mais vendidas")
elif macas < bananas:
    input("As bananas tiveram mais vendidas")
else:
    input("As vendas foram iguais")