macas = int(input("Digite a quantidade de maçãs vendidas: "))
bananas = int(input("Digite a quantidade de maçãs vendidas: "))

if macas > bananas:
    print("As maçãs tiveram mais vendidas")
elif macas < bananas:
    print("As bananas tiveram mais vendidas")
else:
    print("As vendas foram iguais")